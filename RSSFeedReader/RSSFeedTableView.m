- (void)viewWillAppear:(BOOL)animated
{
    [super viewWillAppear:animated];
    
    if ([rssData count]==0) {
        activityView = [[IndicatorViewController alloc]initWithNibName:@"IndicatorViewController" bundle:nil];
        
        [self.view.superview insertSubview:activityView.view aboveSubview:self.view];
    }
   
}

- (void)viewDidAppear:(BOOL)animated
{
    [super viewDidAppear:animated];
    
    if (!rssData || [rssData count] == 0) {
        rssParser = [[RSSXMLParsingDelegate alloc]initWithURL:@"http://www.31days.co.uk/rss.xml"];
        rssData = [rssParser getRSSArray];
        
        [self.tableView reloadData];
    }
    if ([activityView.view superview]) {
        [activityView.view removeFromSuperview];
    }
    
}

